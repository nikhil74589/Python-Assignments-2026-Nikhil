# Calculate total and average of 4 subjects

subject1 = 80

subject2 = 75

subject3 = 90

subject4 = 85 

total_subjects = 4

print(f"Students Marks;")
print(f"subject1 : {subject1}")
print(f"subject2 : {subject2}")
print(f"subject3 : {subject3}")
print(f"subject4 : {subject4}")

total_marks = subject1 + subject2 + subject3 + subject4 
print (f"Total Marks of 4 subjects : {total_marks}")
print("-"*20)
average = total_marks / total_subjects
print(f"Average of 4 subject : {average}")




