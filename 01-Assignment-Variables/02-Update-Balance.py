# Update balance after deposit 
current_balance = 10000 
deposit_balance = 5000 
print(f"Current Balance : {current_balance}")
print(f"Deposit Balance : {deposit_balance}")

current_balance=current_balance+deposit_balance
print(f"After deposit : {current_balance}")

withdraw=200
print(f"Withdraw amount : {withdraw}")
current_balance=current_balance-withdraw
print(f"After Withdraw : {current_balance}")