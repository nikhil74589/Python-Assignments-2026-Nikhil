# Calculate profit or loss percentage

cost_price = 500
selling_price = 600

print(f"The cost price is : {cost_price}")
print(f"The selling price is : {selling_price}")

profit = selling_price - cost_price
print(f"Profit is to be :{profit}")

percentage = (profit / cost_price)*100
print(f"The profit is in (%) : {percentage}")
