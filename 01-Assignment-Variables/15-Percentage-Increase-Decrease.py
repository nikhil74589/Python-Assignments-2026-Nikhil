# Calculate percentage increase or decrease
initial_value = 200
final_value = 250

difference = (final_value - initial_value)

print(f"The Initial Value is : {initial_value}")
print(f"The Final Value is : {final_value}")
print(f"The Diffrence is (Increase): {difference}")
print("-"*50)

percentage_change = (difference/initial_value)*100

print(f"The Percentage of Increased value is : {percentage_change}")